public class even_num {
    public static void main(String[] args){
        int i = 1;
        while(i<=10){
            System.out.println((2*i));
            i++;
            
        }
        //sum of an number
        int n = 1;
        while(n<=10){
            System.out.println((n*(n+1)));
            n++;
        }
    }
}
